<script setup lang="ts">
import { ref, reactive } from 'vue'

const submitted = ref(false)
const submitting = ref(false)
const globalError = ref('')

const form = reactive({
  name: '',
  contact: '',
  service: '',
  message: '',
  budget: '',
})

const errors = reactive({
  name: '',
  contact: '',
  service: '',
  message: '',
})

const serviceOptions = [
  'Graphics Designing',
  'Website Development',
  'Computer Repair',
  'Photo Editing',
  'Online Services (NIDA, etc.)',
  'Laptop Purchase',
  'Other',
]

const budgets = ['< 50k TZS', '50k-200k', '200k-500k', '500k+']

const hours = [
  { days: 'Mon - Fri', time: '8:00 AM - 6:00 PM' },
  { days: 'Saturday',  time: '9:00 AM - 4:00 PM' },
  { days: 'Sunday',    time: 'Closed' },
]

function validate(): boolean {
  let valid = true
  errors.name = errors.contact = errors.service = errors.message = ''
  globalError.value = ''

  if (!form.name.trim()) { errors.name = 'Name is required.'; valid = false }
  if (!form.contact.trim()) { errors.contact = 'Phone or email is required.'; valid = false }
  if (!form.service) { errors.service = 'Please select a service.'; valid = false }
  if (form.message.trim().length < 10) { errors.message = 'Please write a bit more about your project.'; valid = false }

  return valid
}

async function handleSubmit() {
  if (!validate()) return

  submitting.value = true
  globalError.value = ''

  try {
    const response = await fetch('https://formspree.io/f/xkokypwg', {
      method: 'POST',
      headers: {
        'Content-Type': 'application/json',
        'Accept': 'application/json',
      },
      body: JSON.stringify({
        name: form.name,
        contact: form.contact,
        service: form.service,
        message: form.message,
        budget: form.budget || 'Not specified',
      }),
    })

    if (response.ok) {
      submitted.value = true
      Object.assign(form, { name: '', contact: '', service: '', message: '', budget: '' })
    } else {
      const data = await response.json()
      globalError.value = data?.errors?.[0]?.message || 'Something went wrong. Please try again.'
    }
  } catch {
    globalError.value = 'Network error. Please check your connection and try again.'
  } finally {
    submitting.value = false
  }
}
</script>

